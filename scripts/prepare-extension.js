#!/usr/bin/env node
/**
 * Extension build-time config generator (Option B).
 * Generates config.js and config.worker.js for dev or prod.
 *
 * Usage: node scripts/prepare-extension.js [dev|prod]
 * Default: prod
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const env = process.argv[2] || "prod";
const prodUrl = "https://zedi-note.app";
const devUrl = "https://dev.zedi-note.app";
const apiBase = env === "dev" ? devUrl : prodUrl;

const extDir = path.join(__dirname, "..", "extension");

// host_permissions to strip for prod (minimal-privilege build)
const PROD_STRIP_PATTERNS = ["http://localhost/*", "https://api-development-b126.up.railway.app/*"];

const manifestBasePath = path.join(extDir, "manifest.base.json");
const manifestOutPath = path.join(extDir, "manifest.json");
const manifestBase = JSON.parse(fs.readFileSync(manifestBasePath, "utf8"));
if (env === "prod") {
  manifestBase.host_permissions = (manifestBase.host_permissions || []).filter(
    (p) => !PROD_STRIP_PATTERNS.includes(p),
  );
}
fs.writeFileSync(manifestOutPath, JSON.stringify(manifestBase, null, 2) + "\n");

const configContent = `/**
 * Zedi Extension config - generated by prepare-extension.js
 * Do not edit manually. Run: bun run prepare:extension:dev | prepare:extension:prod
 */
window.ZEDI_EXT_CONFIG={API_BASE_URL:"${apiBase}"};
`;

const workerContent = `/**
 * Zedi Extension config (Service Worker) - generated by prepare-extension.js
 * Do not edit manually. Run: bun run prepare:extension:dev | prepare:extension:prod
 */
self.ZEDI_EXT_CONFIG={API_BASE_URL:"${apiBase}"};
`;

fs.writeFileSync(path.join(extDir, "config.js"), configContent);
fs.writeFileSync(path.join(extDir, "config.worker.js"), workerContent);

console.log(`[prepare-extension] ${env}: API_BASE_URL=${apiBase}, manifest.json written`);
